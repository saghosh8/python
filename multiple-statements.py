#The lines can be continued with continuation charactor
sum= 10+20+30+\
     40+50+60+\
     70+80+90
print(sum)


#The lines can be continued with brackets
sum2= (10+20+30+
       40+50+60+
       70+80+90)
print(sum2)


#Line continuation for Str
sum3= ('Rectangle'+
       'Square'+
       'Triangle')
print(sum3)