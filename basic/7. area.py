# write a program to compute the area:


#Finding the Area of Circle
radius = 5
pi = 3.14

Area_Of_Circle = pi * (radius**2)

print(f'Area of Circle is', Area_Of_Circle)


#Finding the Area of Traiangle
a=7
b=8
c=9

s = (a + b + c)/2

Area_of_Triangle = (s*(s-a)*(s-b)*(s-c) )** 0.5
print(f'Area of Triangle is', Area_of_Triangle)


#Finding the Area of Rectangle
Width = 8
Height = 5

Area = Width * Height
print(f'Area of Rectangle is', Area) 


#Finding the Area of Square
Side = 4

Area_Of_Square = Side ** 2
print(f'Area of Square is', Area_Of_Square)

